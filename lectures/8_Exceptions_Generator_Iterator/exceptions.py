class NegativeValueError(Exception):
    def __init__(self, num):
        self.message = f"Negative number: {num}"

    def __str__(self):
        return self.message


def division(a, b):
    try:
        if a < 0 or b < 0:
            raise NegativeValueError(a if a < 0 else b)
        if isinstance(a, float) or isinstance(b, float):
            raise FloatingPointError
        c = a / b
    # except Exception:
    #     print("Exception")
    except NegativeValueError as n:
        print(n)
    except (FloatingPointError, ZeroDivisionError):
        print("Arithmetic error")
    else:
        print("Everything all right!")
        return c
    finally:
        print("Final")


division(2, 3)
